    for (int j = 0; j < jp.getComponentCount(); j++) {
      Component c = jp.getComponent(j);
      if (c instanceof JLabel) {
        JLabel jl = (JLabel) c;
        if (jl.getText().contains("A") || jl.getText().contains("K")) {
          if (wpis) {
            jl.setOpaque(true);
            jl.setBackground(Color.red);
          }
          i++;
          continue;
        }
        if (sekwencje[k][(j - i) / 8][(j - i) % 8] == 0) {
          jl.setOpaque(true);
          jl.setBackground(color);
          wpis = false;
        } else {
          jl.setOpaque(true);
          jl.setBackground(Color.red);
          wpis = true;
        }
      } else {
        i++;
      }
    }
