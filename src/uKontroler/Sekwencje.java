  private void colorM(JLabel jl) {
    if (jl.getBackground() == Color.red) {
      jl.setOpaque(true);
      jl.setBackground(color);
    } else {
      jl.setOpaque(true);
      jl.setBackground(Color.red);
    }
  }

  private void colorMS(JLabel jl, JLabel jlS, Color colS) {
    if (jl.getBackground() == Color.red) {
      jl.setOpaque(true);
      jl.setBackground(color);
      jlS.setOpaque(true);
      jlS.setBackground(colS);
    } else {
      jl.setOpaque(true);
      jl.setBackground(Color.red);
      jlS.setOpaque(true);
      jlS.setBackground(Color.red);
    }

  }

  private String formatuj(String str) {
    boolean fl = false;
    if (str.contains(".")) {
      if (str.length() - str.indexOf(".") > 3) {
        str = str.substring(0, str.length() - str.indexOf("."));
        fl = true;
      } else if (str.length() - str.indexOf(".") < 3) {
        str = str + "0";
        if ((str.length() - str.indexOf(".") < 3)) {
          str = str + "0";
        }
      }
    } else {
      str = str + ".00";
    }
    if (str.length() > 5) {
      str = str.substring(str.length() - 5, str.length());
      fl = true;
    } else if (str.length() < 5) {
      while (str.length() < 5) {
        str = "0" + str;
      }
    }
    if (fl) {
      JOptionPane.showMessageDialog(null, "Uwaga! Zmiana wpisu");
    }
    return str;
  }

  private String Xformatuj(String str) {
    if (str.contains(".")) {
      if (str.length() - str.indexOf(".") > 3) {
        str = str.substring(0, str.length() - str.indexOf("."));
      }
    }
    if (str.length() > 5) {
      str = str.substring(str.length() - 5, str.length());
    } else if (str.length() < 5) {
      while (str.length() < 5) {
        str = "0" + str;
      }
    }
    return str;
  }

